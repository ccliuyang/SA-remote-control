        set(handles.tracetype,'Value',2);
    elseif strcmp(type,'MINH')
        set(handles.tracetype,'Value',3);
    elseif strcmp(type,'AVG')
        set(handles.tracetype,'Value',4);
    elseif strcmp(type,'BLAN')
        set(handles.tracetype,'Value',5);
    end
